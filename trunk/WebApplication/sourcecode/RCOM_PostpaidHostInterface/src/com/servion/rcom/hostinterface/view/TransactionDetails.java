package com.servion.rcom.hostinterface.view;

import com.servion.rcom.hostinterface.util.HostConstants;

public class TransactionDetails {

	private String amount;
	private String pmtReceivedDate;
	private String pmtType;
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getPmtReceivedDate() {
		return pmtReceivedDate;
	}
	public void setPmtReceivedDate(String pmtReceivedDate) {
		this.pmtReceivedDate = pmtReceivedDate;
	}
	public String getPmtType() {
		return pmtType;
	}
	public void setPmtType(String pmtType) {
		this.pmtType = pmtType;
	}
	@Override
	public String toString() {
		return amount + HostConstants.PIPE
				+ pmtReceivedDate + HostConstants.PIPE + pmtType;
	}
	
	
}
