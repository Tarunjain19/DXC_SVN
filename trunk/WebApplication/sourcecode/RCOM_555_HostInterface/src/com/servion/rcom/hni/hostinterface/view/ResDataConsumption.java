package com.servion.rcom.hni.hostinterface.view;

public class ResDataConsumption {

	private String responseCode = null;
	private String consumedData = null;
	private String allocatedData = null;
	
	
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getConsumedData() {
		return consumedData;
	}
	public void setConsumedData(String consumedData) {
		this.consumedData = consumedData;
	}
	public String getAllocatedData() {
		return allocatedData;
	}
	public void setAllocatedData(String allocatedData) {
		this.allocatedData = allocatedData;
	}
	
	
}
