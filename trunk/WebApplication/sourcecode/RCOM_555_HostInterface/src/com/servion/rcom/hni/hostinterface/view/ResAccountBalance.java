package com.servion.rcom.hni.hostinterface.view;

import com.servion.rcom.hni.hostinterface.util.HostConstants;


public class ResAccountBalance{

	private String responseCode = null;
	private String accBalance = null;
	private String accBalanceExpiryDate = null;
	
	
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getAccBalance() {
		return accBalance;
	}
	public void setAccBalance(String accBalance) {
		this.accBalance = accBalance;
	}
	public String getAccBalanceExpiryDate() {
		return accBalanceExpiryDate;
	}
	public void setAccBalanceExpiryDate(String accBalanceExpiryDate) {
		this.accBalanceExpiryDate = accBalanceExpiryDate;
	}
	public String getParamValue()
	{
		return this.accBalance + HostConstants.PIPE + this.accBalanceExpiryDate;
	}
}
