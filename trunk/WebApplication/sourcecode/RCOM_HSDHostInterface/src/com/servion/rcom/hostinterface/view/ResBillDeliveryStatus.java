package com.servion.rcom.hostinterface.view;

import com.servion.rcom.hostinterface.util.HostConstants;



public class ResBillDeliveryStatus {

	private String responseCode = null;
	private String mode = null;
	private String billGenDate = null;
	private String billDeliveryDate = null;
	private String billDeliveryStatus = null;
	
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getBillGenDate() {
		return billGenDate;
	}
	public void setBillGenDate(String billGenDate) {
		this.billGenDate = billGenDate;
	}
	public String getBillDeliveryDate() {
		return billDeliveryDate;
	}
	public void setBillDeliveryDate(String billDeliveryDate) {
		this.billDeliveryDate = billDeliveryDate;
	}
	public String getBillDeliveryStatus() {
		return billDeliveryStatus;
	}
	public void setBillDeliveryStatus(String billDeliveryStatus) {
		this.billDeliveryStatus = billDeliveryStatus;
	}
	@Override
	public String toString() {
		return    mode
				+ HostConstants.PIPE + billGenDate
				+ HostConstants.PIPE + billDeliveryDate
				+ HostConstants.PIPE + billDeliveryStatus;
	}
	
}
