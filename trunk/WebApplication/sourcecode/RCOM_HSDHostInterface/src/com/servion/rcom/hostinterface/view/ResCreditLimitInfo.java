package com.servion.rcom.hostinterface.view;

import com.servion.rcom.hostinterface.util.HostConstants;



public class ResCreditLimitInfo {

	private String responseCode = null;
	private String creditLimit = null;
	private String sysDate = null;
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getCreditLimit() {
		return creditLimit;
	}
	public void setCreditLimit(String creditLimit) {
		this.creditLimit = creditLimit;
	}
	public String getSysDate() {
		return sysDate;
	}
	public void setSysDate(String sysDate) {
		this.sysDate = sysDate;
	}
	@Override
	public String toString() {
		return    creditLimit
				+ HostConstants.PIPE + sysDate;
	}
	
}
