package com.servion.rcom.ppp.hostinterface.view;

import com.servion.rcom.ppp.hostinterface.util.HostConstants;

public class ResUnbilledInfo {

	private String responseCode = null;
	private String unBilledAmount = null;
	private String sysDate = null;
	
	
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getUnBilledAmount() {
		return unBilledAmount;
	}
	public void setUnBilledAmount(String unBilledAmount) {
		this.unBilledAmount = unBilledAmount;
	}
	public String getSysDate() {
		return sysDate;
	}
	public void setSysDate(String sysDate) {
		this.sysDate = sysDate;
	}
	
	@Override
	public String toString() {
		return unBilledAmount
				+ HostConstants.PIPE + sysDate;
	}
	
}
