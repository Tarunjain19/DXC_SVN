package com.servion.rdtv.hostservice;


public class ResAccountDetails {

	private String responseCode = null;
	private String accBalance = null;
	private String unbilledAount = null;
	private String balanceDate = null;
	private String creditLimit = null;
	
	
	public String getUnbilledAount() {
		return unbilledAount;
	}
	public void setUnbilledAount(String unbilledAount) {
		this.unbilledAount = unbilledAount;
	}
	public String getBalanceDate() {
		return balanceDate;
	}
	public void setBalanceDate(String balanceDate) {
		this.balanceDate = balanceDate;
	}
	public String getCreditLimit() {
		return creditLimit;
	}
	public void setCreditLimit(String creditLimit) {
		this.creditLimit = creditLimit;
	}
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getAccBalance() {
		return accBalance;
	}
	public void setAccBalance(String accBalance) {
		this.accBalance = accBalance;
	}
	
	@Override
	public String toString() {
		return "ResAccountDetails :: responseCode=" + responseCode
				+ ", accBalance=" + accBalance + ", unbilledAount="
				+ unbilledAount + ", balanceDate=" + balanceDate
				+ ", creditLimit=" + creditLimit;
	}
	
	
}
