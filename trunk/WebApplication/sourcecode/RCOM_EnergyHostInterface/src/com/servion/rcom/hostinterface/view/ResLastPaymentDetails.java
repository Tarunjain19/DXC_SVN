package com.servion.rcom.hostinterface.view;

public class ResLastPaymentDetails {

	private String responseCode = null;
	private boolean isValidAccount = false;
	private String totalAmtReceived = null;
	private String lastPaymentReceivedDate = null;
	private String modeOfPayment = null;
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public boolean isValidAccount() {
		return isValidAccount;
	}
	public void setValidAccount(boolean isValidAccount) {
		this.isValidAccount = isValidAccount;
	}
	public String getTotalAmtReceived() {
		return totalAmtReceived;
	}
	public void setTotalAmtReceived(String totalAmtReceived) {
		this.totalAmtReceived = totalAmtReceived;
	}
	public String getLastPaymentReceivedDate() {
		return lastPaymentReceivedDate;
	}
	public void setLastPaymentReceivedDate(String lastPaymentReceivedDate) {
		this.lastPaymentReceivedDate = lastPaymentReceivedDate;
	}
	public String getModeOfPayment() {
		return modeOfPayment;
	}
	public void setModeOfPayment(String modeOfPayment) {
		this.modeOfPayment = modeOfPayment;
	}
	
}
